#Ejercicio de operadores

#Declaración Variables

a=3#operador1
b=4#operador2
c=a+b#resultado suma

print("La suma de {operador1} y {operador2} es {resultado}".format(operador1=a,operador2=b,resultado=c))

c=a-b#resultado resta
print("La resta de {operador1} y {operador2} es {resultado}".format(operador1=a,operador2=b,resultado=c))

c=a*b#resultado producto
print("El producto de {operador1} y {operador2} es {resultado}".format(operador1=a,operador2=b,resultado=c))

c=a/b#resultado fracción
print("La fracción de {operador1} y {operador2} es {resultado}".format(operador1=a,operador2=b,resultado=c))

